/**
* \brief De-serializes the RQB from the HIF shared memory into the hosts local memory - according to the component ID and OP-code in the RQB.
*
* @param [in] hSysDev           - system handle 
* @param [in] pHifPipeHandle    - pipe handle
* @param [in] pRqbSrc           - pointer to the serialized RQB in the transfer memory
* @param [out] ppRqbDest        - pointer to the RQB deserialized in the hosts local memory
*
* @return HIF_OK                - success
* @return HIF_ERR_RESOURCE      - Not enough local memory
*/
LSA_UINT16      hif_hsa_deserialize_upper (
        HIF_SYS_HANDLE hSysDev,
        LSA_VOID* pHifPipeHandle,
        LSA_VOID* pRqbSrc,
        LSA_VOID** ppRqbDest )
{
    LSA_UINT8* help_ptr;
    HSA_RQB_TYPE* new_rqb;
    LSA_OPCODE_TYPE opcode;
    LSA_VOID* ptr_rqb_src = (LSA_VOID*)pRqbSrc;
    HSA_RQB_TYPE** ptr_ptr_rqb_dest = (HSA_RQB_TYPE**)ppRqbDest;
    LSA_RESPONSE_TYPE response = 0;
    LSA_UINT32 i=0;
    LSA_UNUSED_ARG(hSysDev);
    LSA_UNUSED_ARG(pHifPipeHandle);

    HIF_ASSERT(HIF_IS_NOT_NULL(ptr_ptr_rqb_dest)); /*#17*/
    HIF_ASSERT(HIF_IS_NOT_NULL(ptr_rqb_src));

    HIF_DESER_GET_OPCODE(ptr_rqb_src, opcode);

    help_ptr = (LSA_UINT8*)ptr_rqb_src + HIF_SER_SIZEOF(4);  /* start deserialization after the original upper RQB pointer */

    new_rqb = (HSA_RQB_TYPE*)hif_get_rqb_id(ptr_rqb_src); /* get orig. upper RQB */
    HIF_PROGRAM_TRACE_03(0,LSA_TRACE_LEVEL_NOTE_LOW,"hif_hsa_deserialize_upper() opc(%d) shm_addr(0x%08x) pRQB(0x%08x)",opcode,ptr_rqb_src,new_rqb+4);

    if (opcode==HSA_OPC_OPEN_CHANNEL)
    {

        /* restore the Upper RQB */
        HIF_RQB_SET_OPCODE(new_rqb,opcode);                /* _opcode */
        help_ptr+=4;                /* skip opcode on shared-mem */
        HIF_SER_RESPONSE_SHMTH(&response,(LSA_UINT32**)&help_ptr);                /* _response */
        HIF_RQB_SET_RESPONSE(new_rqb,response);                                /* _response */
    }
    else if (opcode==HSA_OPC_CLOSE_CHANNEL)
    {

        /* restore the Upper RQB */
        HIF_RQB_SET_OPCODE(new_rqb,opcode);                /* _opcode */
        help_ptr+=4;                /* skip opcode on shared-mem */
        HIF_SER_RESPONSE_SHMTH(&response,(LSA_UINT32**)&help_ptr);                /* _response */
        HIF_RQB_SET_RESPONSE(new_rqb,response);                                /* _response */
    }
    else if (opcode==HSA_OPC_START_INSTANCE)
    {

        /* restore the Upper RQB */
        HIF_RQB_SET_OPCODE(new_rqb,opcode);                /* _opcode */
        help_ptr+=4;                /* skip opcode on shared-mem */
        HIF_SER_RESPONSE_SHMTH(&response,(LSA_UINT32**)&help_ptr);                /* _response */
        HIF_RQB_SET_RESPONSE(new_rqb,response);                                /* _response */
        HIF_SER_COPY_SHMTH_16(&new_rqb->args.start.instanceHandle,(LSA_UINT32**)&help_ptr);        /* instanceHandle */
    }
    else if (opcode==HSA_OPC_STOP_INSTANCE)
    {

        /* restore the Upper RQB */
        HIF_RQB_SET_OPCODE(new_rqb,opcode);                /* _opcode */
        help_ptr+=4;                /* skip opcode on shared-mem */
        HIF_SER_RESPONSE_SHMTH(&response,(LSA_UINT32**)&help_ptr);                /* _response */
        HIF_RQB_SET_RESPONSE(new_rqb,response);                                /* _response */
    }
    else if (opcode==HSA_OPC_GET_STATISTICS)
    {

        /* restore the Upper RQB */
        HIF_RQB_SET_OPCODE(new_rqb,opcode);                /* _opcode */
        help_ptr+=4;                /* skip opcode on shared-mem */
        HIF_SER_RESPONSE_SHMTH(&response,(LSA_UINT32**)&help_ptr);                /* _response */
        HIF_RQB_SET_RESPONSE(new_rqb,response);                                /* _response */
        HIF_SER_COPY_SHMTH_32(&new_rqb->args.statistics.outFrameLow,(LSA_UINT32**)&help_ptr);        /* outFrameLow */
        HIF_SER_COPY_SHMTH_32(&new_rqb->args.statistics.outFrameHigh,(LSA_UINT32**)&help_ptr);        /* outFrameHigh */
        HIF_SER_COPY_SHMTH_32(&new_rqb->args.statistics.inFrameLow,(LSA_UINT32**)&help_ptr);        /* inFrameLow */
        HIF_SER_COPY_SHMTH_32(&new_rqb->args.statistics.inFrameHigh,(LSA_UINT32**)&help_ptr);        /* inFrameHigh */
        HIF_SER_COPY_SHMTH_32(&new_rqb->args.statistics.inDuplicateLow,(LSA_UINT32**)&help_ptr);        /* inDuplicateLow */
        HIF_SER_COPY_SHMTH_32(&new_rqb->args.statistics.inDuplicateHigh,(LSA_UINT32**)&help_ptr);        /* inDuplicateHigh */
        for(i=0;i<HSA_MAX_PORT;i++)                        /* port*/
        {
            HIF_SER_COPY_SHMTH_32(&new_rqb->args.statistics.port[i].outFrameLow,(LSA_UINT32**)&help_ptr);            /* outFrameLow */
            HIF_SER_COPY_SHMTH_32(&new_rqb->args.statistics.port[i].outFrameHigh,(LSA_UINT32**)&help_ptr);            /* outFrameHigh */
            HIF_SER_COPY_SHMTH_32(&new_rqb->args.statistics.port[i].inFrameLow,(LSA_UINT32**)&help_ptr);            /* inFrameLow */
            HIF_SER_COPY_SHMTH_32(&new_rqb->args.statistics.port[i].inFrameHigh,(LSA_UINT32**)&help_ptr);            /* inFrameHigh */
            HIF_SER_COPY_SHMTH_32(&new_rqb->args.statistics.port[i].LowPassedLow,(LSA_UINT32**)&help_ptr);            /* LowPassedLow */
            HIF_SER_COPY_SHMTH_32(&new_rqb->args.statistics.port[i].LowPassedHigh,(LSA_UINT32**)&help_ptr);            /* LowPassedHigh */
            HIF_SER_COPY_SHMTH_32(&new_rqb->args.statistics.port[i].HighPassedLow,(LSA_UINT32**)&help_ptr);            /* HighPassedLow */
            HIF_SER_COPY_SHMTH_32(&new_rqb->args.statistics.port[i].HighPassedHigh,(LSA_UINT32**)&help_ptr);            /* HighPassedHigh */
        }
    }
    else if (opcode==HSA_OPC_SEND_SINGLE_FRAME)
    {

        /* restore the Upper RQB */
        HIF_RQB_SET_OPCODE(new_rqb,opcode);                /* _opcode */
        help_ptr+=4;                /* skip opcode on shared-mem */
        HIF_SER_RESPONSE_SHMTH(&response,(LSA_UINT32**)&help_ptr);                /* _response */
        HIF_RQB_SET_RESPONSE(new_rqb,response);                                /* _response */
    }
    else if (opcode==HSA_OPC_RECEIVE_SINGLE_FRAME)
    {

        /* restore the Upper RQB */
        HIF_RQB_SET_OPCODE(new_rqb,opcode);                /* _opcode */
        help_ptr+=4;                /* skip opcode on shared-mem */
        HIF_SER_RESPONSE_SHMTH(&response,(LSA_UINT32**)&help_ptr);                /* _response */
        HIF_RQB_SET_RESPONSE(new_rqb,response);                                /* _response */
        HIF_SER_COPY_SHMTH_16(&new_rqb->args.recv.ringPort,(LSA_UINT32**)&help_ptr);        /* ringPort */
        if (HIF_IS_NOT_NULL_U32_REF_BY_PTR(help_ptr) && new_rqb->args.recv.frameLength > 0 ) /*#22d*/
        {
            help_ptr+=HIF_SER_SIZEOF(4); /* skip buffer-pointer */
            if (HIF_IS_NOT_NULL(new_rqb->args.recv.pFramebuf)) /*#24*/
            {
                HIF_SER_COPY_SHMTH_BUF(new_rqb->args.recv.pFramebuf,&help_ptr,new_rqb->args.recv.frameLength);        /* pFramebuf */
            }
        }
        else
        {
            help_ptr+=HIF_SER_SIZEOF(4);          /* skip buffer-pointer */
        }
    }
    else if (opcode==HSA_OPC_CANCEL_RECEIVE)
    {

        /* restore the Upper RQB */
        HIF_RQB_SET_OPCODE(new_rqb,opcode);                /* _opcode */
        help_ptr+=4;                /* skip opcode on shared-mem */
        HIF_SER_RESPONSE_SHMTH(&response,(LSA_UINT32**)&help_ptr);                /* _response */
        HIF_RQB_SET_RESPONSE(new_rqb,response);                                /* _response */
    }
    else
    {
        HIF_PROGRAM_TRACE_02(0,LSA_TRACE_LEVEL_FATAL,"hif_hsa_deserialize_upper(). RQB-Opcode:0x%x is not valid! shm_addr(0x%08x)", opcode, ptr_rqb_src);
        HIF_FATAL(0);
    }
    *ptr_ptr_rqb_dest = new_rqb;

    /* Check for overwrite */
    HIF_SHM_CHECK_OVERWRITE_DESERIALIZE(ptr_rqb_src, opcode, help_ptr, "hif_hsa_deserialize_upper");

    return HIF_OK;
}

