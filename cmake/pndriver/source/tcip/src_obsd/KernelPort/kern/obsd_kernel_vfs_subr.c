/* Vnode reference. */
void
vref(struct vnode *vp)
{
	vp->v_usecount++;
}

/*
 * Vnode release - use for active VNODES.
 * If count drops to zero, call inactive routine and return to freelist.
 * Returns 0 if it did not sleep.
 */
int
vrele(struct vnode *vp)
{
	vp->v_usecount--;
	if (vp->v_usecount > 0) {
		return (0);
	}

	return (1);
}
